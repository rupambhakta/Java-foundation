public class swap {
    public static void main(String[] args) {
        int a=2;
        int b=5;
        System.out.println("The value of a is "+a);
        System.out.println("The value of b is "+b);
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("The value of a is "+a);
        System.out.println("The value of b is "+b);

    }
}
